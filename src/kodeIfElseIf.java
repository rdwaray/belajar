public class kodeIfElseIf {
    public static void main(String[] args) {
        int num = 6;

        // If-else if statement
        if (num > 0) {
            System.out.println("Number is positive");
        } else if (num < 0) {
            System.out.println("Number is negative");
        } else {
            System.out.println("Number is zero");
        }
    }
}
